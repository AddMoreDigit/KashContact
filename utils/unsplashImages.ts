// ==================== HELPER FUNCTIONS ====================

/**
 * Get a random avatar from the collection
 */
export function getRandomAvatar(): string {
  const avatarKeys = Object.keys(avatars) as Array<keyof typeof avatars>;
  const randomKey = avatarKeys[Math.floor(Math.random() * avatarKeys.length)];
  return avatars[randomKey];
}

/**
 * Get an avatar by index (1-8)
 */
export function getAvatar(index: number): string {
  const key = `avatar${Math.min(Math.max(index, 1), 8)}` as keyof typeof avatars;
  return avatars[key];
}

/**
 * Get multiple avatars for a team/group
 */
export function getTeamAvatars(count: number): string[] {
  return Array.from({ length: count }, (_, i) => getAvatar(i + 1));
}

/**
 * Get a campaign image based on category
 */
export function getCampaignImage(category: string): string {
  const categoryMap: Record<string, string> = {
    birthday: campaigns.birthday,
    anniversary: campaigns.anniversary,
    graduation: campaigns.graduation,
    wedding: activities.wedding,
    corporate: campaigns.corporate,
    team: campaigns.teamEvent,
    family: campaigns.familyTrip,
    honeymoon: campaigns.honeymoon,
    default: campaigns.teamEvent,
  };
  
  const lowerCategory = category.toLowerCase();
  return categoryMap[lowerCategory] || categoryMap.default;
}

